var $Nu = jQuery.noConflict();

function equalHeights() {
	//Equal height blocks
	var blocks = $Nu('.equal');
	var maxHeight = 0;
	blocks.each(function(){
		maxHeight = Math.max(maxHeight, parseInt($Nu(this).css('height')));
	});
	blocks.css('height', maxHeight);

}

function loginBlock() {
	if ($Nu("header .k2LoginMod").length){
		//The login Area toggling (sets the position dynamically)
		var height = $Nu("header .k2LoginMod").height();
		$Nu("header .k2LoginMod").css("top",-height);

		$Nu(".openSlide").click(function(){
			$Nu("header .k2LoginMod").stop().animate({top:0},500);
			$Nu(".openSlide").toggle();
			$Nu(".closeSlide").toggle();
		});

		$Nu(".closeSlide").click(function(){
			$Nu("header .k2LoginMod").stop().animate({top:-height},500);
			$Nu(".openSlide").toggle();
			$Nu(".closeSlide").toggle();
		});
	}
}

$Nu(document).ready(function() {

	//jQueryTools tabs
	$Nu("ul.TabsNavigation").tabs("div.itemTabs > div", {
		effect: 'slide',
		rotate: true
	});

	//jQueryTools tabs
	$Nu("ul.moduleTabs").tabs("div.tabbedModules > div");

	$Nu(".menuToggler").click(function(e){
		e.preventDefault();
		$Nu("body").toggleClass('activeMenu');
		$Nu("html").toggleClass('menuIsOpen');
	});

	$Nu(".menuCloseButton").click(function(e){
		e.preventDefault();
		$Nu("body").toggleClass('activeMenu');
		$Nu("html").toggleClass('menuIsOpen');
	});

});

//Equal Heights for the footer blocks
$Nu(window).load(function () {
	//the equal heights functions
	equalHeights();
	//position the login block
	loginBlock();
});

$Nu(window).resize(function () {
	// run the equal heights script and the login block on resize as well
	setTimeout( function(){
		equalHeights();
		loginBlock();
	} , 500);

});
